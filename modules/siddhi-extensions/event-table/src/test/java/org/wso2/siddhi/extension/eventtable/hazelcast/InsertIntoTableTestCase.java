    @Before
    public void init() {
        inEventCount.set(0);
        removeEventCount = 0;
        eventArrived = false;
        inEventsList = new ArrayList<Object[]>();
        instances = new ArrayList<String>();
        for (HazelcastInstance instance : Hazelcast.getAllHazelcastInstances()) {
            instances.add(instance.getName());
        }
    }

    @After
    public void cleanup() {
        for (HazelcastInstance instance : Hazelcast.getAllHazelcastInstances()) {
            if (!instances.contains(instance.getName())) {
                log.info("shutting down : " + instance.getName());
                instance.shutdown();
            }
        }
    }

    @Test
    public void insertIntoTableTest1() throws InterruptedException {
        log.info("InsertIntoTableTest1");

        SiddhiManager siddhiManager = new SiddhiManager();
        String streams = "" +
                "define stream StockStream (symbol string, price float, volume long); " +
                "@from(eventtable = 'hazelcast')" +
                "define table StockTable (symbol string, price float, volume long); ";
        String query = "" +
                "@info(name = 'query1') " +
                "from StockStream " +
                "insert into StockTable ;";

        ExecutionPlanRuntime executionPlanRuntime = siddhiManager.createExecutionPlanRuntime(streams + query);
        try {
            InputHandler stockStream = executionPlanRuntime.getInputHandler("StockStream");

            executionPlanRuntime.start();
            stockStream.send(new Object[]{"WSO2", 55.6f, 100l});
            stockStream.send(new Object[]{"IBM", 75.6f, 100l});
            stockStream.send(new Object[]{"WSO2", 57.6f, 100l});
            Thread.sleep(RESULT_WAIT);
        } finally {
            executionPlanRuntime.shutdown();
        }
    }

